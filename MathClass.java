package BasicJavaProgram;

public class MathClass 
{
	public static void main(String[] args) {
		System.out.println(Math.addExact(100, 200));
		System.out.println(Math.addExact(3456784444l, 234565432));
		System.out.println(Math.subtractExact(100, 1000));
		System.out.println(Math.multiplyExact(10, 10));
		System.out.println(Math.min(10, 1452));
		System.out.println(Math.max(2.3, 6.3));
		System.out.println(Math.abs(-800));
}
}
